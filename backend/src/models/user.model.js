import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true,
    },
    fullName: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
        minlength: 6,
    },
    profilePic: {
        type: String,
        default: "",
    },
},
    {
        timestamps: true
    }
)

//creating a model
const User = mongoose.model("User", userSchema); // name should be singular and name start with capital letter

export default User;

